  private boolean ValidarDados(){
        try {
            if(txtEmissao.getDate()==null){
                MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "INFORME A DATA DO INÍCIO DO CONTRATO", getTitle());
                return false;
            }
            if(txtTermimoContrato.getDate()==null){
                MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "INFORME A DATA DO TÉRMINO DO CONTRATO", getTitle());
                return false;
            }
            
            
            
            return true;
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
            return false;
            
        }
    }
