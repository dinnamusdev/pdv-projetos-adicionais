    private void btIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIncluirActionPerformed
        // TODO add your handling code here:
        try {
            if(ValidarDados()){               
                boolean bRet = false;                        
                if(nIDContratoAtual!=0){            
                    if(MetodosUI_Auxiliares.MensagemAoUsuarioOpcoes_Sim_e_Nao(null, "FOI IDENTIFICADO UM CONTRATO ANTERIOR EM VIGÊNCIA\nO PROCEDIMENTO SOLICITADO REALIZARÁ O TERMINO AUTOMÁTICO DESSE CONTRATO\n\nDESEJA CRIAR UM NOVO CONTRATO ?", "RENOVAÇÃO CONTRATUAL")==MetodosUI_Auxiliares.Sim()){    
                        bRet=GerenciadorLicenca.Contrato_Renovar(contrato,  0, 0,nIDContratoAtual);
                    }else{
                        return;
                    }
                }else{
                    bRet = GerenciadorLicenca.Contrato_Persistir(contrato, 0, 0);
                }
                if(bRet){
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesINFO(null, "PROCEDIMENTO REALIZADO COM SUCESSO", getTitle());
                    this.dispose();
                }else{
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "NÃO FOI POSSIVEL REALIZAR O PROCEDIMENTO SOLICITADO", getTitle());
                }
            }
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
        }
    }//GEN-LAST:event_btIncluirActionPerformed

