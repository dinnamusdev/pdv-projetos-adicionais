    public frmLicencaDetalhes(java.awt.Frame parent, boolean modal,int IDContratante,String Contratante, int IDContrato) {
        super(parent, modal);        
        this.IDContratante = IDContratante;
        this.Contratante = Contratante;
        
        
        if(GerenciadorLicenca.Contrato_Contar(IDContratante)==0){
            NovoContrato(IDContratante);
        }else{                    
            try {
                 if(IDContrato==0){
                    ResultSet rsContratos = GerenciadorLicenca.Contrato_Listar(IDContratante,"ATIVO");
                    if(rsContratos.last()){
                        nIDContratoAtual = rsContratos.getInt("idcontrato");
                        contrato = GerenciadorLicenca.Contrato_Pesquisar(IDContratante,nIDContratoAtual);                   
                        contrato = GerenciadorLicenca.Gerar_Contrato_Renovado(contrato,0,0, 1);                                     
                    }else{
                         NovoContrato(IDContratante);
                    }             
                 }else{
                    contrato = GerenciadorLicenca.Contrato_Pesquisar(IDContratante,IDContrato);                                                      
                 }
            } catch (SQLException ex) {
                LogDinnamus.Log(ex, true);
            }
        }
        initComponents();
       
        this.setLocationRelativeTo(null);
    }

