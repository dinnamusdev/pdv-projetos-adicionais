 private boolean IniciarUI(){
        try {
               
            txtContrante.setText(Contratante);
            int nIdRep=0;               
            if(contrato.getIdrepresentante()!=null){
              nIdRep = contrato.getIdrepresentante();
            }
            MetodosUI_Auxiliares.PreencherCombo(cbRepresentante, GerenciadorLicenca.Representante_Listar(), "razao", "idrepresentante",true);
            if(nIdRep!=0){
               MetodosUI_Auxiliares.SetarOpcaoCombo_Model(cbRepresentante, nIdRep);
            }
            controleteclas.TRocarTabPorEnter();
            BindComponentes("CARREGANDO CONTRATO DE LICENÇA");
            
            
            
             if(!contrato.getStatus().equalsIgnoreCase("ATIVO")){
                btIncluir.setEnabled(false);
            }else{
                 if(contrato.getIdcontrato()!=null){
                    if(GerenciadorLicencaFinanceiro.ListarParcelas(contrato.getIdcontrato(),true).next()){
                       MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "EDIÇÃO BLOQUEADA. O CONTRATO POSSUI FINANCEIRO COM MOVIMENTO. ", Contratante);
                       btIncluir.setEnabled(false);
                    }else{
                       btIncluir.setEnabled(true);
                    }
                 }
            }
             
            
                
           return true;
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
            return false;
        }
    }
   private void BloquearFrame(boolean status){
        this.setEnabled(status);
    
    }
  private void BindComponentes(String MSG) {
        
        try {
              InteracaoComUsuarioDuranteProcesso.ExibirMensagem("Contrato de Licenciamento",MSG); 
              new Thread(){
                        @Override                       
                        public void run(){     
                                BloquearFrame(false);
                                beansbind.BindAuto(txtIDContrato, contrato, "idcontrato","text");
                                beansbind.BindAuto(txtLicRet, contrato, "licret","text");
                                beansbind.BindAuto(txtLicPDV1, contrato, "licpdv","text");
                                beansbind.BindAuto(txtLicPre, contrato, "licpre","text");
                                beansbind.BindAuto(txtEmissao, contrato, "dataemissao","date");
                                beansbind.BindAuto(txtInicioContrato1, contrato, "iniciocontrato","date");
                                beansbind.BindAuto(txtTermimoContrato, contrato, "termimnocontrato","date");
                                beansbind.BindAuto(txtPeriodicidade, contrato, "periodicidadelib","text");
                                beansbind.BindAuto(txtPrazoExpirar, contrato, "prazoexpirarlic","text");
                                beansbind.BindAuto(txtValorMensal, contrato, "valorcontrato","text");
                                beansbind.BindAuto(cbRepresentante, contrato, "idrepresentante","selectedItem", new ConverterIntegerEmItemLista());
                                beansbind.BindAuto(cbStatus, contrato, "status","selectedItem");                                    
                                beansbind.BindAuto(txtDiaVencimento, contrato, "vencimento","text");
                                beansbind.BindAuto(txtPrimeiroVencimento, contrato, "primeirovencimento","date");
                                beansbind.BindAuto(txtNumeroParcelas, contrato, "numeroparcelas","text");
                                InteracaoComUsuarioDuranteProcesso.FecharMensagem();
                                BloquearFrame(true);
                        }
               }.start();
        } catch (Exception e) {
        }
        
      
    }
