    private void btRealizarPagtoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRealizarPagtoActionPerformed
        // TODO add your handling code here:
        try {
            if(MetodosUI_Auxiliares.MensagemAoUsuarioOpcoes_Sim_e_Nao(null, "CONFIRMA A BAIXA DA PARCELA ?", getTitle())==MetodosUI_Auxiliares.Sim()){
                if(GerenciadorLicencaFinanceiro.BaixaParcela(IdParcela, new Float(txtValorPago.getValue().toString()), new Date(txtDataPagto.getValue().toString()), cbMeioPagto.getSelectedItem().toString())){
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesINFO(null, "PARCELA PAGA COM SUCESSO", getTitle());
                    this.dispose();
                }else{
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "NÃO FOI POSSÍVEL BAIXAR A PARCELA", getTitle());
                }
            }            
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
        }
    }//GEN-LAST:event_btRealizarPagtoActionPerformed

