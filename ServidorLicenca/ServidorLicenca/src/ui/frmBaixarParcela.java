    private boolean Iniciar(){
        try {
            ResultSet rs = GerenciadorLicencaFinanceiro.Parcela(IdParcela);
            if(rs.next()){
                
                txtValorParcela.setValue(rs.getFloat("valor"));
                txtVencimento.setValue(rs.getDate("vencimento"));
                txtValorPago.setValue(rs.getFloat("valor"));
               //new Formate
                String cData=DataFormatar.Formatar_Data_Em_String_DDMMYYYY(new Date());
               txtDataPagto.setValue(cData);
               txtDadosContrato.setText(Dados);
                //txtDataPagto2 = new JFormattedTextField(new MaskFormatter("##/##/####"));
                
            }
            
            return true;
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
            return false;
        }
    }
