    private boolean Iniciar(){
        try {
            ResultSet rs = GerenciadorLicencaFinanceiro.Parcela(IDParcela);
            if(rs.next()){                
               txtValor.setValue(rs.getFloat("valor"));
                
               String cData=DataFormatar.Formatar_Data_Em_String_DDMMYYYY(rs.getDate("emissao"));
               txtEmissao.setValue(cData);
               
               cData=DataFormatar.Formatar_Data_Em_String_DDMMYYYY(rs.getDate("vencimento"));
               txtVencimento.setValue(cData);              
               
               cbStatus.setSelectedItem(rs.getString("status"));
            }            
            return true;
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
            return false;
        }
    }

