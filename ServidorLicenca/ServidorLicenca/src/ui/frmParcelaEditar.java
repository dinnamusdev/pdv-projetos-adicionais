    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        // TODO add your handling code here:
        try {
            
            if(MetodosUI_Auxiliares.MensagemAoUsuarioOpcoes_Sim_e_Nao(null, "CONFIRMA A EDIÇÃO DOS DADOS DA PARCELA", getTitle())==MetodosUI_Auxiliares.Sim()){
                
                Float Valor = Float.valueOf(txtValor.getValue().toString());
                Date Emissao = DataFormatar.Formatar_String_DDMMYYYY_Em_Data(txtEmissao.getValue().toString());
                Date Vencimento = DataFormatar.Formatar_String_DDMMYYYY_Em_Data(txtVencimento.getValue().toString());
                
                if(GerenciadorLicencaFinanceiro.ParcelaEditar(IDParcela, Valor, Emissao, Vencimento,cbStatus.getSelectedItem().toString())){
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesINFO(null, "PARCELA EDITADA COM SUCESSO", getTitle());
                    this.dispose();
                }else{
                    MetodosUI_Auxiliares.MensagemAoUsuarioSimplesAVISO(null, "NÃO FOI POSSÍVEL EDITAR A PARCELA", getTitle());
                }
                
            
            }
            
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
        }
    }//GEN-LAST:event_btGravarActionPerformed

